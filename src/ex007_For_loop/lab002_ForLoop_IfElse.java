package ex007_For_loop;

public class lab002_ForLoop_IfElse {
    public static void main(String[] args) {

        for(int i = 0; i<18; i++){
            if(i>16){
                System.out.println("IS NOW Valid");
            }
            else {
                System.out.println("IS NOT Valid");
            }
        }
    }
}
