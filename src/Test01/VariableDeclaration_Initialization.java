package Test01;



    public class VariableDeclaration_Initialization {
        public static void main(String[] args) {
            // Declare and initialize variables using appropriate data types and naming conventions

            int age = 25;                  // int for whole numbers
            double value_ofPia = 3.14;         // double for decimal numbers
            boolean ATB = true;      // boolean for true/false
            char grade = 'A';              // char for single characters

            // Print the values of all variables
            System.out.println("Integer: " + age);
            System.out.println("Double: " + value_ofPia);
            System.out.println("Boolean: " + ATB);
            System.out.println("Character: " + grade);
        }
    }


