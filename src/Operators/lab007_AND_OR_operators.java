package Operators;

public class lab007_AND_OR_operators {
    public static void main(String[] args) {
        System.out.println(true&&true);
        System.out.println(true&&false);
        System.out.println(true&&false);
        System.out.println(false&&false);


        System.out.println(true||true);
        System.out.println(true||false);
        System.out.println(true||false);
        System.out.println(false||false);

    }

}
