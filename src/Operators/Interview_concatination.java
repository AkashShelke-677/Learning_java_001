package Operators;

public class Interview_concatination {
    public static void main(String[] args) {
        System.out.println("Sri"+"latha");
        System.out.println("Sri"+"Harsha");

        int a=29;
        int b=1;
        System.out.println(a+b);
        System.out.println("a+b");



        String first_name= "sri";
        String last_name= "Latha";

        int c=10;
        int d=20;
        System.out.println(first_name+last_name+c+d);
        System.out.println(c+d+first_name+last_name);

        System.out.println(first_name+last_name+(c+d));
        System.out.println((c+d)+first_name+last_name);

    }
}
